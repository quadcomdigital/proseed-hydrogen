type Translations = typeof translations;

function deepGet(obj: Record<string, unknown> | TranslationValue, path: string[]): TranslationValue | undefined {
  let current: unknown = obj;
  for (const key of path) {
    if (current && typeof current === 'object' && key in current) {
      current = (current as Record<string, unknown>)[key];
    } else {
      return undefined;
    }
  }
  if (current && typeof current === 'object' && 'it' in current && 'en' in current) {
    return current as TranslationValue;
  }
  return undefined;
}

export function t(key: string, lang: Lang): string {
  const parts = key.split('.');
  const value = deepGet(translations as unknown as Record<string, unknown>, parts);
  if (!value) return key;
  return value[lang] || value.it;
}

